<?php

namespace App\Models;

use Illuminate\Database\Eloquent\Factories\HasFactory;
use Illuminate\Database\Eloquent\Model;
use Illuminate\Database\Eloquent\Relations\BelongsTo;
use Illuminate\Database\Eloquent\Relations\HasOne;

class Scout extends Model
{
    use HasFactory;

    public const STATUS_PENDING = 0;
    public const STATUS_IN_PROGRESS = 1;
    public const STATUS_CLOSED = 2;

    protected $fillable = [
        'company_id',
        'corporate_id',
        'job_id',
        'message',
        'status',
    ];

    protected $casts = [
        'job_id' => 'integer',
        'status' => 'integer',
    ];

    /**
     * ????????????E?????E     * ????: ????????????E??????????
     */
    public function company(): BelongsTo
    {
        return $this->belongsTo(Company::class);
    }

    /**
     * ?????????????????E?????E     * ????: ???????????????????
     */
    public function corporate(): BelongsTo
    {
        return $this->belongsTo(Corporate::class);
    }

    /**
     * ????????????E?????????E     * ????: ???E??????????????E??????????
     */
    public function job(): BelongsTo
    {
        return $this->belongsTo(Job::class);
    }

    /**
     * ??E????????????E???E???E?????E?????E????E     * ?????Ethreads ? scout_id ???E????E     * ?company_id + freelancer_id + job_id?E?Eullable?E?????E????E     * ????: ??????E??E??????????????
     */
    public function thread(): HasOne
    {
        $relation = $this->hasOne(Thread::class, 'company_id', 'company_id')
            ->where('corporate_id', $this->corporate_id);

        if ($this->job_id === null) {
            return $relation->whereNull('job_id');
        }

        return $relation->where('job_id', $this->job_id);
    }
}